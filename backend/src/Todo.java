/**
 * A Todo object for the todo list.
 */
public class Todo {
    private  int id;
    private String item;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getItem() {
        return this.item;
    }

    public void setItem(String item) {
        this.item = item;
    }
}
